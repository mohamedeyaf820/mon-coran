/* helpers */
export function getSurah(n) {
  return SURAHS[n - 1];
}

const AR_DIGITS = ["٠", "١", "٢", "٣", "٤", "٥", "٦", "٧", "٨", "٩"];
export function toAr(num) {
  return String(num)
    .split("")
    .map((d) => AR_DIGITS[+d])
    .join("");
}

export function surahName(n, lang = "ar") {
  const s = SURAHS[n - 1];
  if (lang === "fr") return s.fr;
  if (lang === "en") return s.en;
  return s.ar;
}
